class ValueofDemo
{
 public static void main(String args[])
 {
  int intvar=10;
  float floatvar=40.4f;
  double doublevar=123.5;
  boolean booleanvar=true;
  System.out.println("intvar="+String.valueOf(intvar));
  System.out.println("floatvar="+String.valueOf(floatvar));
  System.out.println("doublevar="+String.valueOf(doublevar)); 
  System.out.println("booleanvar="+String.valueOf(booleanvar));
 }
} 
  
 