class DemoAsOP1
{
 public static void main(String args[])
 {
  int a=10;
  System.out.println(a);       //assignment is done here 
  System.out.println(a=30);    //  in print statement 
  System.out.println(a);        // value is updated
  System.out.println((a=10)==30);  
  System.out.println(a);
  }
 }
 